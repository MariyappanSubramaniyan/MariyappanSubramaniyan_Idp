# Python Range Function --> generate a sequence of numbers

# Using `range()` with One Argument
for i in range(5):
    print(i)
# Output:
# 0
# 1
# 2
# 3
# 4

# Using `range()` with Two Arguments
for i in range(1, 6):
    print(i)
# Output:
# 1
# 2
# 3
# 4
# 5

# Using `range()` with Three Arguments
for i in range(2, 10, 2):
    print(i)
# Output:
# 2
# 4
# 6
# 8


# Using Negative Step in `range()`
for i in range(10, 0, -2):
    print(i)
# Output:
# 10
# 8
# 6
# 4
# 2


