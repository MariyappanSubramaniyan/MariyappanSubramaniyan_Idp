# Python Strings

# Strings in Python are sequences of characters enclosed in either single quotes or double quotes.
# They are immutable, meaning once created, they cannot be changed.

# Creating Strings

# Using Quotes
str1 = 'Hello'
str2 = "World"

# Multiline Strings
text = """This is a
multiline string."""

# String Operations

# Concatenation - joining of strings
a = "Python"
b = "Rocks"
c = a + " " + b  # Output: Python Rocks

# Repetition
text = "Hi! " * 3  # Output: Hi! Hi! Hi! 






